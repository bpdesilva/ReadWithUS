    private void initUserSelectComboBoxes() {
        cmbxUsers.removeAllItems();
        String userType = cmbxUsersType.getSelectedItem().toString();
        switch (userType) {
            case "Librarian":
                if (!librariansList.isEmpty()) {
                    librariansList.clear();
                }
                librariansList = l.getLibrarians();
                for (int i = 0; i < librariansList.size(); i++) {
                    String[] librarian = (String[]) librariansList.get(i);
                    cmbxUsers.addItem(librarian[1]);
                }
                break;
            case "Member":
                if (!membersList.isEmpty()) {
                    membersList.clear();
                }
                membersList = m.getMembers();
                for (int i = 0; i < membersList.size(); i++) {
                    String[] member = (String[]) membersList.get(i);
                    cmbxUsers.addItem(member[1]);
                }
                break;
        }
    }

    private void initAuthorSelectComboBoxes() {
        if (!authsList.isEmpty()) {
            authsList.clear();
        }
        authsList = au.getAuth();
        cmbxAuth.removeAllItems();
        cmbxAuth1.removeAllItems();
        for (int i = 0; i < authsList.size(); i++) {
            String[] author = (String[]) authsList.get(i);
            cmbxAuth.addItem(author[1]);
            cmbxAuth1.addItem(author[1]);
        }
    }

    private void initAllLoginUsersComboBox() {
        if (!loginsList.isEmpty()) {
            loginsList.clear();
        }
        loginsList = u.getlogins();
        cmbxlogins.removeAllItems();
        for (int i = 0; i < loginsList.size(); i++) {
            String[] loginUser = (String[]) loginsList.get(i);
            cmbxlogins.addItem(loginUser[2] + " - " + loginUser[0]);
        }
    }

