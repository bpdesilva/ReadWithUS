    private void btnAddUsersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddUsersActionPerformed
        if (txtFirstName.getText().equals("") && txtLastName.getText().equals("") && txtNIC.getText().equals("") && txtEmail.getText().equals("") && txtPhoneNo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "All Feild are required ! ");
        } else {
            String ph = txtPhoneNo.getText();
            String em = txtEmail.getText();
            String nic = txtNIC.getText();
            if (!ph.matches(regexph) && !em.matches(EMAIL_REGEX) && !nic.matches(nicregex)) {
                JOptionPane.showMessageDialog(null, "Please enter valid data!");
            } else {
                String type = cmbxUserType.getSelectedItem().toString();
                switch (type) {
                    case "Librarian":
                        l.setFname(txtFirstName.getText());
                        l.setLname(txtLastName.getText());
                        l.setNic(txtNIC.getText());
                        l.setEmail(txtEmail.getText());
                        l.setPhone(txtPhoneNo.getText());
                        l.addLibrarian();
                        break;
                    case "Member":
                        m.setFname(txtFirstName.getText());
                        m.setLname(txtLastName.getText());
                        m.setNic(txtNIC.getText());
                        m.setEmail(txtEmail.getText());
                        m.setPhone(txtPhoneNo.getText());
                        m.addMember();
                        break;
                    default:
                        JOptionPane.showMessageDialog(null, "Please select an option!");
                        break;
                }
                JOptionPane.showMessageDialog(null, "Data saved successful!");
                txtFirstName.setText("");
                txtLastName.setText("");
                txtNIC.setText("");
                txtEmail.setText("");
                txtPhoneNo.setText("");
            }
        }
    }//GEN-LAST:event_btnAddUsersActionPerformed

