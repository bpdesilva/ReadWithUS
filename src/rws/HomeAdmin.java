    private void btnAddBooksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddBooksActionPerformed
        if (txtRefId.getText().equals("") && txtBookName.getText().equals("") && txtGenre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "All Feild are required ! ");
        } else {
            String auth = cmbxAuth.getSelectedItem().toString();
            int AId = -1;
            for (int i = 0; i < authsList.size(); i++) {
                String[] author = (String[]) authsList.get(i);
                if (auth.equals(author[1])) {
                    AId = Integer.parseInt(author[0]);
                    break;
                }
            }

            b.setRefId(Integer.parseInt(txtRefId.getText()));
            b.setBookName(txtBookName.getText());
            b.setAuthId(AId);
            b.setGenre(txtGenre.getText());
            b.Addbook(b);
            JOptionPane.showMessageDialog(null, "Book Added successful!");
            txtRefId.setText("");
            txtBookName.setText("");
            txtGenre.setText("");
            cmbxAuth.setSelectedIndex(0);
            txtRefId.requestFocusInWindow();
        }
    }//GEN-LAST:event_btnAddBooksActionPerformed

